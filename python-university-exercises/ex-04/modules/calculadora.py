class Calculadora:

    def __init__(self):
        pass

    def suma(self, num1, num2):
        return num1 + num2

    def resta(self, num1, num2):
        return num1 - num2

    def multiplicacion(self, num1, num2):
        return num1 * num2

    def division(self, num1, num2):
        return num1 / num2

    def division_entera(self, num1, num2):
        return num1 // num2

    def exponente(self, num1, num2):
        return num1**num2

    def modulo(self, num1, num2):
        return num1 % num2
